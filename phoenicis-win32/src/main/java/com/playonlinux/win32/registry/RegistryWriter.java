package com.playonlinux.win32.registry;

public class RegistryWriter {
    private String buildRegTreeString(AbstractRegistryNode node) {
        final StringBuilder generatedRegFileBuilder = new StringBuilder();
        generatedRegFileBuilder.append(node.toRegString()).append("\n");

        if(node instanceof RegistryKey) {
            for (AbstractRegistryNode child : ((RegistryKey) node).getChildren()) {
                generatedRegFileBuilder.append(buildRegTreeString(child));
            }
        }
        return generatedRegFileBuilder.toString();
    }

    public String generateRegFileContent(AbstractRegistryNode node) {
        return String.join("\n", "WINE REGISTRY Version 2",
                ";; Generated by PlayOnLinux", buildRegTreeString(node));
    }
}
